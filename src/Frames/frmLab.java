    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        rdogroup.add(rdoid);
        rdogroup.add(rdoname);

        l.getAllRows(tbllab);
        clearData();
        tbllab.getTableHeader().setReorderingAllowed(false);
        tbllab.getTableHeader().setBackground(new Color(0, 0, 100));
        tbllab.getTableHeader().setForeground(Color.WHITE);
        this.getContentPane().setBackground(new Color(0,51,51));
    }//GEN-LAST:event_formWindowOpened

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String strSsearch = "select * from  labs_data where ";
        if (rdoid.isSelected()) {
            strSsearch += "Lab_No ='" + txtSearch.getText()+"'";
        } else {
            strSsearch += "Lab ='" + txtSearch.getText() + "'";
        }
        l.getCustomRows(strSsearch, tbllab);
    }//GEN-LAST:event_btnSearchActionPerformed

